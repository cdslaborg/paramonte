        call disp%show("call setCov(cov, lowDia, mean, sample, dim, rweight, rweisum)")
                        call setCov(cov, lowDia, mean, sample, dim, rweight, rweisum)
        call disp%show("cov")
        call disp%show( cov )
        call disp%show("cov = getFilled(0., ndim, ndim)")
                        cov = getFilled(0., ndim, ndim)
